minha_lista = [20, 40, 60, 80, 100, 120]
minha_lista.pop() # Remove o último valor da lista
print(minha_lista)
# Resultado esperado = minha_lista = [20, 40, 60, 80, 100]

minha_lista.pop(1) # Remove o ultimo valor da lista
print(minha_lista)
#Resultado esperado = minha_lista = [20, 40, 60, 80, 100]

minha_lista.remove(80) # Remove o valor inserido na lista
print(minha_lista)
#Resultado esperado = minha_lista = [20, 40, 60, 100, 120]
